package ru.mbelin.sweater.bean;

public class BeanKeys {
    private String googleCaptchaSecret;
    private String googleCaptchaURL;


    public String getGoogleCaptchaSecret() {
        return googleCaptchaSecret;
    }

    public void setGoogleCaptchaSecret(String googleCaptchaSecret) {
        this.googleCaptchaSecret = googleCaptchaSecret;
    }

    public String getGoogleCaptchaURL() {
        return googleCaptchaURL;
    }

    public void setGoogleCaptchaURL(String googleCaptchaURL) {
        this.googleCaptchaURL = googleCaptchaURL;
    }
}
